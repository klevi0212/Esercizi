interface Props {
	onClick: () => void
	text: string
	color?: string
	disabled?: boolean
}
export default function Answer(props: Props) {
	const style = props.color ? { color: props.color } : {}

	return (
		<>
			<button onClick={props.onClick} disabled={props.disabled} style={style}>
				{props.text}
			</button>
		</>
	)
}
